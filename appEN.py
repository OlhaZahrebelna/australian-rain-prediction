import streamlit as st
import pandas as pd
import joblib

st.set_page_config(
    page_title="Rain Prediction in Australia",
    page_icon="🌦️",
)

@st.cache_resource
def load_artifacts():
    return joblib.load("models/aussie_rain.joblib")

artifacts = load_artifacts()

model = artifacts["model"]
input_cols = artifacts["input_cols"]
numeric_cols = artifacts["numeric_cols"]
categorical_cols = artifacts["categorical_cols"]
encoded_cols = artifacts["encoded_cols"]
imputer = artifacts["imputer"]
if not hasattr(imputer, "_fill_dtype"):
    imputer._fill_dtype = imputer.statistics_.dtype

scaler = artifacts["scaler"]
encoder = artifacts["encoder"]

st.title("🌦️ Rain Prediction for Tomorrow")

st.markdown("""
This application uses a **Random Forest** model
to predict whether it will rain tomorrow in Australia.
""")

with st.expander("ℹ️ Input Data Explanation"):
    st.markdown("""
- **Location** — city or weather station in Australia.
- **MinTemp / MaxTemp** — minimum and maximum daily temperature (°C).
- **Rainfall** — precipitation amount in millimeters.
- **WindGustDir** — direction of the strongest wind gust.
- **WindDir9am / WindDir3pm** — wind direction at 9:00 AM and 3:00 PM.
- **Humidity9am / Humidity3pm** — relative humidity (%).
- **Pressure9am / Pressure3pm** — atmospheric pressure (hPa).
- **RainToday** — whether it rained today.
""")

    
with st.expander("📖 Step-by-Step User Guide"):
    st.markdown("""
### How to Use the Application

1. **Select a location** from the list of available Australian cities or weather stations.

2. **Enter the current day's weather measurements**, including:
   - minimum and maximum temperature;
   - rainfall amount;
   - wind direction and speed;
   - humidity;
   - atmospheric pressure;
   - other required parameters.

3. **Specify whether it rained today** in the **"RainToday"** field:
   - **Yes** — if there was rainfall today;
   - **No** — if there was no rainfall.

4. **Verify that the entered data is correct.** The more accurate the inputs, the more reliable the model prediction will be.

5. **Click the "Predict" button.**

6. **Review the result**, which includes:
   - the prediction of whether it will rain tomorrow (**Yes** or **No**);
   - the prediction probability as a percentage.

> ⚠️ Note: The prediction is generated by a machine learning model based on historical meteorological data and is intended for informational purposes only. It is not an official weather forecast.
""")
    
    
locations = [
    "Albury", "BadgerysCreek", "Cobar", "CoffsHarbour", "Moree",
    "Newcastle", "NorahHead", "NorfolkIsland", "Penrith", "Richmond",
    "Sydney", "SydneyAirport", "WaggaWagga", "Canberra", "Melbourne",
    "MelbourneAirport", "Hobart", "Brisbane", "Cairns", "GoldCoast",
    "Townsville", "Adelaide", "Perth", "PerthAirport", "Darwin",
    "AliceSprings", "MountGambier", "Ballarat", "Bendigo", "Launceston"
]

wind_dirs = [
    "N", "NNE", "NE", "ENE", "E", "ESE", "SE", "SSE",
    "S", "SSW", "SW", "WSW", "W", "WNW", "NW", "NNW"
]

user_input = {}

st.subheader("Enter Weather Data")

user_input["Location"] = st.selectbox("📍 Location", locations)

user_input["MinTemp"] = st.number_input("🌡️ Minimum Temperature (°C)", min_value=-50.0, value=15.0)
user_input["MaxTemp"] = st.number_input("☀️ Maximum Temperature (°C)", min_value=-50.0, value=25.0)
user_input["Rainfall"] = st.number_input("🌧️ Rainfall (mm)", min_value=0.0, value=0.0)
user_input["Evaporation"] = st.number_input("Evaporation", min_value=0.0, value=5.0)
user_input["Sunshine"] = st.number_input("Sunshine", min_value=0.0, value=8.0)

user_input["WindGustDir"] = st.selectbox("Direction of the Strongest Wind Gust", wind_dirs)
user_input["WindGustSpeed"] = st.number_input("Strongest Wind Gust Speed (km/h)", min_value=0.0, value=35.0)

user_input["WindDir9am"] = st.selectbox("Wind Direction at 9:00 AM", wind_dirs)
user_input["WindDir3pm"] = st.selectbox("Wind Direction at 3:00 PM", wind_dirs)

user_input["WindSpeed9am"] = st.number_input("🍃 Wind Speed at 9:00 AM (km/h)", min_value=0.0, value=15.0)
user_input["WindSpeed3pm"] = st.number_input("🍃 Wind Speed at 3:00 PM (km/h)", min_value=0.0, value=20.0)

user_input["Humidity9am"] = st.number_input("💧 Humidity at 9:00 AM (%)", min_value=0.0, max_value=100.0, value=60.0)
user_input["Humidity3pm"] = st.number_input("💧 Humidity at 3:00 PM (%)", min_value=0.0, max_value=100.0, value=50.0)

user_input["Pressure9am"] = st.number_input("📈 Atmospheric Pressure at 9:00 AM (hPa)", min_value=0.0, value=1015.0)
user_input["Pressure3pm"] = st.number_input("📈 Atmospheric Pressure at 3:00 PM (hPa)", min_value=0.0, value=1013.0)

user_input["Cloud9am"] = st.number_input("Cloud Cover at 9:00 AM", min_value=0.0, max_value=9.0, value=4.0)
user_input["Cloud3pm"] = st.number_input("Cloud Cover at 3:00 PM", min_value=0.0, max_value=9.0, value=4.0)

user_input["Temp9am"] = st.number_input("Temperature at 9:00 AM (°C)", min_value=-50.0, value=18.0)
user_input["Temp3pm"] = st.number_input("Temperature at 3:00 PM (°C)", min_value=-50.0, value=23.0)

user_input["RainToday"] = st.selectbox("☔ Did It Rain Today?", ["No", "Yes"])

def preprocess_input(user_input):
    input_df = pd.DataFrame([user_input])
    input_df = input_df[input_cols]

    input_df[numeric_cols] = imputer.transform(input_df[numeric_cols])
    input_df[numeric_cols] = scaler.transform(input_df[numeric_cols])

    encoded = encoder.transform(input_df[categorical_cols])
    encoded_df = pd.DataFrame(encoded, columns=encoded_cols, index=input_df.index)

    final_df = pd.concat(
        [input_df.drop(columns=categorical_cols), encoded_df],
        axis=1
    )

    return final_df


if st.button("Predict"):

    # =========================
    # Input data validation
    # =========================

    # Minimum temperature must be lower than maximum temperature
    if user_input["MinTemp"] >= user_input["MaxTemp"]:
        st.error("❌ Minimum temperature must be lower than maximum temperature.")
        st.stop()

    # Temperature at 9:00 AM must be between MinTemp and MaxTemp
    if not (user_input["MinTemp"] <= user_input["Temp9am"] <= user_input["MaxTemp"]):
        st.error("❌ Temperature at 9:00 AM must be between the minimum and maximum temperature.")
        st.stop()

    # Temperature at 3:00 PM must be between MinTemp and MaxTemp
    if not (user_input["MinTemp"] <= user_input["Temp3pm"] <= user_input["MaxTemp"]):
        st.error("❌ Temperature at 3:00 PM must be between the minimum and maximum temperature.")
        st.stop()

    # If there was no rain today, rainfall should be 0
    if user_input["RainToday"] == "No" and user_input["Rainfall"] > 0:
        st.error("❌ You selected that there was no rain today, but rainfall is greater than 0 mm.")
        st.stop()

    # Wind speed cannot be greater than the maximum wind gust speed
    if user_input["WindSpeed9am"] > user_input["WindGustSpeed"]:
        st.error("❌ Wind speed at 9:00 AM cannot exceed the maximum wind gust speed.")
        st.stop()

    if user_input["WindSpeed3pm"] > user_input["WindGustSpeed"]:
        st.error("❌ Wind speed at 3:00 PM cannot exceed the maximum wind gust speed.")
        st.stop()

    # =========================
    # Prediction
    # =========================

    final_input = preprocess_input(user_input)

    prediction = model.predict(final_input)[0]
    probabilities = model.predict_proba(final_input)[0]

    yes_probability = probabilities[
        list(model.classes_).index("Yes")
    ]

    if prediction == "Yes":
        st.success("🌧️ Prediction: it will rain tomorrow")
    else:
        st.success("☀️ Prediction: it will not rain tomorrow")

    st.info(f"Probability of rain tomorrow: {yes_probability:.2%}")